<?php

/**
 * New mb Functions - PHP 8.4
*/

/*
|--------------------------------------------------------------------------
| mb_ucfirst
|--------------------------------------------------------------------------
*/

var_dump( mb_ucfirst('football') );
// string(8) "Football"

/*
|--------------------------------------------------------------------------
| mb_lcfirst
|--------------------------------------------------------------------------
*/

var_dump( mb_lcfirst('FOOTBALL') );
// string(8) "fOOTBALL"

/*
|--------------------------------------------------------------------------
| mb_trim | mb_ltrim | mb_rtrim
|--------------------------------------------------------------------------
*/

$text = '!Abc!';

var_dump( mb_trim($text, '!') );
// string(3) "Abc"

var_dump( mb_ltrim($text, '!') );
// string(4) "Abc!"

var_dump( mb_rtrim($text, '!') );
// string(4) "!Abc"
